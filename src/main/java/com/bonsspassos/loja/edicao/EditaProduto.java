    public void preencheCampos(List<Produto> produtos,Object valor, String atributo) {

        Map<Object, Produto> mapProduto = new HashMap<>();
        for (Produto produto : produtos) {
            switch (atributo) {
                case "ID":
                    mapProduto.put(valor, produto);
                   break;
                case "Produto":
                    mapProduto.put(valor, produto);
                    break;
                case "Marca":
                    mapProduto.put(valor, produto);
                    break;
                case "Departamento":
                    mapProduto.put(valor, produto);
                   break;
                case "Modelo":
                    mapProduto.put(valor, produto);
                   break;
                case "Tamanho":
                    mapProduto.put(valor, produto);
                   break;
                case "Quantidade":
                    mapProduto.put(valor, produto);
                   break;
                case "Valor":
                    mapProduto.put(valor, produto);
                   break;
                
            }
            Produto result = mapProduto.get(valor);
            nomeProduto.setText(result.getNomeProduto());
            marcaProduto.setSelectedItem(result.getMarcaProduto());
            departamentoProd.setSelectedItem(result.getDepartamentoProd());
            modeloProduto.setSelectedItem(result.getModeloProduto());
            tamanhoProduto.setSelectedItem(result.getTamanhoProduto());
            qtdProduto.setValue(result.getQtdProduto());
            valorProduto.setText(result.getValorProduto());
        }
        
    }
    
    public List<String> validaCamposProduto(
            JTextField nomeProduto, 
            JTextField valorProduto
    ) {
        List<String> camposNulos = new ArrayList<String>();

        if (String.valueOf(nomeProduto.getText()).isBlank() || nomeProduto.getText() == null) {
            camposNulos.add("O campo produto deve ser preenchido!");
        }
        if (valorProduto.getText() == null) {
            camposNulos.add("O campo CPF deve ser preenchido!");
        }
        return camposNulos;
    }
    
